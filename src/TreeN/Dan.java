package TreeN;

public class Dan {
    private String maXa;
    private String CCCD;
    private String ten;
    private String ngaySinh;

    public Dan(String maXa, String CCCD, String ten, String ngaySinh) {
        this.maXa = maXa;
        this.CCCD = CCCD;
        this.ten = ten;
        this.ngaySinh = ngaySinh;
    }

    public String getMaXa() {
        return maXa;
    }

    public String getCCCD() {
        return CCCD;
    }

    public String getTen() {
        return ten;
    }

    public String getNgaySinh() {
        return ngaySinh;
    }
}
